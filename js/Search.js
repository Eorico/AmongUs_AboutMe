const form = document.querySelector("form");
const input = document.querySelector(".search-input");
const resultsList = document.getElementById("results-list");
const noResults = document.getElementById("no-results");

 
form.addEventListener("submit", function (e) {
    e.preventDefault();
    const query = input.value.trim().toLowerCase();
    const results = data.filter(color =>
        Object.values(color).some(val =>
            String(val).toLowerCase().includes(query)
        )
    );

    resultsList.innerHTML = "";
    noResults.style.display = "none";

    if (results.length > 0) {
        results.forEach(color => {
            const item = document.createElement("li");
            item.className = "list-group-item";
            item.innerHTML = `
                <div class="among-us-character" style="background-color: ${color.RGB}"></div>
                <div>
                    <strong>ID:</strong> ${color.UI_ID} <br>
                    <strong>Name:</strong> ${color.NAME} <br>
                    <strong>RGB:</strong> ${color.RGB} <br>
                    <strong>Symbol:</strong> ${color.SYMBOL} <br>
                    <strong>Branding Usage:</strong> ${color.USAGE_FOR_BRANDING} <br>
                    <strong>Category:</strong> ${color.CATEGORY} <br>
                    <strong>Meaning:</strong> ${color.MEANING || "N/A"}
                </div>
            `;
            resultsList.appendChild(item);
        });
    } else {
        noResults.textContent = `No Crewmates results found for "${query}"`;
        noResults.style.display = "block";
    }
});